CEnemy::CEnemy()
{
	m_iType = ENT_USER_00;
}
// -----------------------------------------------------------------------------
CEnemy::~CEnemy()
{
}
// -----------------------------------------------------------------------------
void CEnemy::Init()
{
	BuildBV();
}
// -----------------------------------------------------------------------------
bool CEnemy::bDamage(float _fEnergy)
{
	m_fEnergy -= _fEnergy;	
	if (m_fEnergy<0.0f)
	{
		m_fEnergy = 0.0f;
		return(true);
	}
	else
		return(false);
}
